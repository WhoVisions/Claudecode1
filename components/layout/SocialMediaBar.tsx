export function SocialMediaBar() {
  const [isVisible, setIsVisible] = useState(true);
  const [isMobileOrTablet, setIsMobileOrTablet] = useState(false);

  useEffect(() => {
    const checkViewport = () => {
      setIsMobileOrTablet(window.innerWidth < 1024);
    };

    checkViewport();
    window.addEventListener('resize', checkViewport);

    return () => window.removeEventListener('resize', checkViewport);
  }, []);

  useEffect(() => {
    let lastScrollY = window.scrollY;

    const handleScroll = () => {
      const currentScrollY = window.scrollY;
      setIsVisible(currentScrollY < lastScrollY || currentScrollY < 100);
      lastScrollY = currentScrollY;
    };

    window.addEventListener('scroll', handleScroll, { passive: true });
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  return (
    <div
      className={`fixed z-40 transition-all duration-500 ${
        isMobileOrTablet
          ? `bottom-4 left-1/2 -translate-x-1/2 ${
              isVisible ? 'translate-y-0 opacity-100' : 'translate-y-32 opacity-0'
            }`
          : 'left-4 top-1/2 -translate-y-1/2'
      }`}
    >
      <div
        className={`bg-background/80 backdrop-blur-xl border border-primary/10 shadow-2xl ${
          isMobileOrTablet
            ? 'flex-row rounded-full px-2 py-2'
            : 'flex-col rounded-2xl px-3 py-4'
        } flex items-center gap-2`}
      >
        {socialLinks.map((social, index) => (
          <a
            key={social.name}
            href={social.href}
            target="_blank"
            rel="noopener noreferrer"
            className={`group relative w-12 h-12 flex items-center justify-center rounded-xl transition-all duration-300 hover:scale-110 ${social.color}`}
            aria-label={social.name}
            style={{
              animationDelay: `${index * 50}ms`,
            }}
          >
            <div className="absolute inset-0 bg-gradient-to-r from-blue-500/10 to-purple-500/10 rounded-xl opacity-0 group-hover:opacity-100 transition-opacity duration-300" />
            <div className="relative text-muted-foreground group-hover:text-current transition-colors duration-300">
              {social.icon}
            </div>

            {/* Tooltip */}
            {!isMobileOrTablet && (
              <div className="absolute left-full ml-4 px-3 py-2 bg-background/95 backdrop-blur-sm border border-primary/10 rounded-lg shadow-xl opacity-0 group-hover:opacity-100 pointer-events-none transition-opacity duration-200 whitespace-nowrap">
                <span className="text-sm font-medium">{social.name}</span>
                <div className="absolute right-full top-1/2 -translate-y-1/2 mr-0.5">
                  <div className="w-2 h-2 bg-background border-l border-t border-primary/10 transform rotate-45" />
                </div>
              </div>
            )}
          </a>
        ))}
      </div>
    </div>
  );
}
